#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	n;

	if (s)
	{
		i = 0;
		while (s[i])
			write(fd, &s[i++], 1);
		n = '\n';
		write(fd, &n, 1);
	}
}
