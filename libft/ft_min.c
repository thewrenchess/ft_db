#include <string.h>

int		ft_min(int *n, size_t l)
{
	int		min;
	size_t	i;

	if (!n)
		return (0);
	min = *n;
	i = 1;
	while (i++ < l)
	{
		n++;
		if (*n < min)
			min = *n;
	}
	return (min);
}
