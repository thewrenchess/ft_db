#include <unistd.h>

void	ft_putendl(char const *s)
{
	int		i;
	char	n;

	if (s)
	{
		i = 0;
		while (s[i])
			write(1, &s[i++], 1);
		n = '\n';
		write(1, &n, 1);
	}
}
